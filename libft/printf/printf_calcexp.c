#include "ft_printf.h"

int		printf_calcexp(long double *n)
{
	int		exp;

	exp = 0;
	if (!n || !*n)
		return (0);
	while (*n >= 10 || *n <= -10)
	{
		exp++;
		*n /= 10;
	}
	while (*n < 1 || *n > -1)
	{
		exp--;
		*n *= 10;
	}
	return (exp);
}
