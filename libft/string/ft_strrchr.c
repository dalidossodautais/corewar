#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = ft_strlen(s) - 1;
	while (i >= 0 && s[i] != c)
		i--;
	if (i == -1)
		return (NULL);
	return ((char*)(s + i));
}
